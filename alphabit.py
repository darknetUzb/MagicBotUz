class Alphabit:
    def __init__(self):
        pass
    def get_letter(self,letter):
        return globals()[letter.lower()]
    def get_word(self,word):
        a_word = ""
        for i in word:
            if i.lower() in 'abcdefghijklmn':
                a_word+=self.get_letter(i)+"\n"
        return a_word
